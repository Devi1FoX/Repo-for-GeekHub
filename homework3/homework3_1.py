"""Створити цикл від 0 до ... (вводиться користувачем). 
В циклі створити умову, яка буде виводити поточне значення, якщо остача від ділення на 17 дорівнює 0."""

nl = []

a = int(input("Enter some number: "))

for x in range(0, a):
    if (x%17==0):
        nl.append(str(x))
print (','.join(nl))